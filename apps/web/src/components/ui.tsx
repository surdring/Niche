interface ButtonProps extends ComponentPropsWithoutRef<"button"> {
  variant?: "primary" | "icon";
}

export const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(({ variant = "primary", style, children, ...props }, ref) => {
  const baseStyle: CSSProperties = {
    cursor: "pointer",
    display: "flex",
    alignItems: "center",
    gap: "6px",
    border: "none",
    borderRadius: "6px",
    fontWeight: 500,
  };

  const styles: Record<string, CSSProperties> = {
    primary: {
      backgroundColor: theme.textMain,
      color: "#fff",
      padding: "6px 16px",
      fontSize: "13px",
    },
    icon: {
      background: "transparent",
      color: theme.textSec,
      padding: "4px",
      borderRadius: "4px",
    }
  }

  return (
    <button ref={ref} style={{ ...baseStyle, ...styles[variant], ...style }} {...props}>
      {children}
    </button>
  );
});
Button.displayName = "Button";

